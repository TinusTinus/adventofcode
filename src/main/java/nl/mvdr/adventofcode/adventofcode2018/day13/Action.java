/**
 * Possible actions a mine cart can take when at an intersection.
 *
 * @author Martijn van de Rijdt
 */
enum Action {
    /** Keep travelling in the same direction. */
    GO_STRAIGHT,
    /** Turn left. */
    TURN_LEFT,
    /** Turn right. */
    TURN_RIGHT;
}
